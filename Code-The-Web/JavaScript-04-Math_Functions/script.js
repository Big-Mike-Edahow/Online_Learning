alert(1 + 2);
// Equal to 3

alert(2.5 + 3.5);
// Equal to 6

alert(-2 + -3);
// Equal to -5

alert(7 + 1.25);
// Equal to 8.25

alert(5 - 3);
// Equal to 2

alert(33 - 42);
// Equal to -9

alert(-3.3 - 1.1);
// Equal to -4.4

alert(1 * 3);
// Equal to 3

alert(9 * 8);
// Equal to 72

alert(-2.23 * 7.92);
// Equal to -17.6616

alert(-4 * -3);
// Equal to 12

alert(1 / 2);
// Equal to 0.5

alert(20 / -4);
// Equal to -5

alert(0 / 5);
// Equal to 0

alert(64 / 0);
// Equal to Infinity

alert(24 % 5);
// Equal to 4

alert(10 % 4);
// Equal to 2

alert(30 % 3);
// Equal to 0, 3 goes into 30 ten times without a remainder

alert(6 % 5);
// Equal to 1

alert(Math.pow(5,2));
// Equal to 25

alert(Math.pow(3,3)); // 3 to the power of 3
// Equal to 27

alert(Math.pow(2,10)); // 2 to the power of 10
// Equal to 1024

/* Square root &
   Cube root */

alert(Math.sqrt(25));
// Equal to 5

alert(Math.cbrt(8));
// Equal to 2

/* Round */

alert(Math.round(35.82562));
// Equal to 36

alert(Math.round(35.22353));
// Equal to 35

alert(Math.round(4));
// Equal to 4 (already rounded)

alert(Math.round(6.5));
// Equal to 7 (.5 rounds up)

/* Floor & Ceiling */

alert(Math.floor(35.82562));
// Equal to 35

alert(Math.ceil(35.82562));
// Equal to 36

alert(Math.ceil(4.0));
// Equal to 4

alert(Math.ceil(4.01));
// Equal to 5

alert(Math.floor(0.99999));
// Equal to 0

